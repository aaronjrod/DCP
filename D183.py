# You type a URL in your browser and press Enter

# Browser looks up IP address for the domain using DNS

# Browser initiates TCP connection with the server
    # Transmission Control Protocol: A communications standard that enables 
    # application programs and computing devices to exchange messages over 
    # a network. It is designed to send packets across the internet and 
    # ensure the successful delivery of data and messages over networks.

# Browser sends the HTTP request to the server
    # Hypertext Transfer Protocol (HTTP) is an application-layer protocol 
    # for transmitting hypermedia documents, such as HTML. It was designed 
    # for communication between web browsers and web servers, but it can 
    # also be used for other purposes

# Server processes request and sends back a response

# Browser renders the content